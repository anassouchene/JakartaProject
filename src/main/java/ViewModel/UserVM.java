package ViewModel;

public class UserVM {
	
	private String Login;
	private String Password;
	
	
	public String getLogin() {
		return Login;
	}
	
	
	public void setLogin(String login) {
		Login = login;
	}
	
	
	public String getPassword() {
		return Password;
	}
	
	
	public void setPassword(String password) {
		this.Password = password;
	}
}
